#Membership Operators
my_string="Spoorthi"
my_string1="Spoorthig"
print("p" in my_string)
print("p" not in my_string)

print(("S" in my_string) and ("S" in my_string1))
print(("g" in my_string) or ("g" in my_string1))
print(not("g" in my_string))

